def city_country(city,country):
	'''return a city with his country'''
	city_country = city + " " + country
	return city_country

city_country_1 = city_country('shanghai', 'china')
print (city_country_1)

city_country_2 = city_country('tianjin', 'china')
print (city_country_2)

city_country_3 = city_country('tokyo', 'japanese')
print (city_country_3)
